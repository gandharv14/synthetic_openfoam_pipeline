def instruction_html(path: Path) -> str:
    if not path.exists():
        return "              <p>Instruction file is not available for this generated task.</p>"
    return "\n".join(f"              <p>{html.escape(line) or '&nbsp;'}</p>" for line in path.read_text().splitlines())


def expected_metrics_items(expected_metrics: dict) -> list[str]:
    items = []
    for key, value in sorted(expected_metrics.items()):
        if value in (None, [], {}, ""):
            continue
        safe_key = html.escape(str(key))
        safe_value = html.escape(json.dumps(value) if isinstance(value, (dict, list)) else str(value))
        items.append(f"<code>{safe_key}</code>: <code>{safe_value}</code>")
    return items or ["Generated scorer metadata is available in the task directory."]


def hidden_variant_axes(hidden_variants: list[dict]) -> list[str]:
    axes: list[str] = []
    for variant in hidden_variants:
        for axis in (variant.get("changes") or {}):
            if axis not in axes:
                axes.append(axis)
    return axes


def render_boundary_condition(spec: TaskRenderSpec, frames_dir: Path, frames: int) -> None:
    np, plt = plotting_modules()
    x, y, xx, yy = channel_grid(np)
    for i, phase in enumerate(frame_phases(frames)):
        broken = 1.0 - 0.68 * xx + 0.24 * np.sin(4.0 * np.pi * xx + phase) * np.exp(-((xx - 1.75) / 0.35) ** 2)
        repaired = 1.0 - 0.48 * xx + 0.025 * np.sin(2.0 * np.pi * yy + phase) * np.exp(-((xx - 1.85) / 0.6) ** 2)
        speed = 1.0 + 0.15 * np.sin(2.0 * np.pi * (xx - 0.25 * phase / math.tau)) * (yy * (1 - yy) * 4)

        fig, axes = plt.subplots(2, 1, figsize=(12.8, 7.2), dpi=100, constrained_layout=True)
        panels = [
            (axes[0], broken, "Broken outlet: pressure reflection and flux mismatch", "reversed outlet patch"),
            (axes[1], repaired, "Oracle repair: smooth pressure drop and balanced flux", "fixed pressure outlet"),
        ]
        for ax, field, title, note in panels:
            im = ax.contourf(x, y, field, levels=32, cmap="viridis")
            ax.streamplot(x, y, speed, np.zeros_like(speed), color="white", density=0.75, linewidth=0.7, arrowsize=0.8)
            ax.text(1.84, 0.88, note, color="white", fontsize=10, ha="right", weight="bold")
            ax.set_xlim(0, 2)
            ax.set_ylim(0, 1)
            ax.set_aspect("equal")
            ax.set_xticks([])
            ax.set_yticks([])
            ax.set_title(title, fontsize=12)
            fig.colorbar(im, ax=ax, fraction=0.024, pad=0.012, label="relative pressure")
        fig.suptitle(report_title(spec, "boundary-condition consistency"), fontsize=15, weight="bold")
        save_frame(fig, frames_dir, i)


def render_solver_convergence(spec: TaskRenderSpec, frames_dir: Path, frames: int) -> None:
    np, plt = plotting_modules()
    iters = np.arange(1, 81)
    broken = 0.45 * np.exp(-iters / 44) + 0.035 * (1 + np.sin(iters * 0.45))
    broken += 0.16 / (1 + np.exp(-(iters - 47) / 4))
    repaired = 0.45 * np.exp(-iters / 9.5) + 2.5e-5
    threshold = float(spec.expected_metrics.get("residual_max", 1e-4))

    for i, frac in enumerate(frame_fracs(frames)):
        n = max(2, int(frac * len(iters)))
        fig, (ax0, ax1) = plt.subplots(1, 2, figsize=(12.8, 7.2), dpi=100, constrained_layout=True)
        field_x, field_y, xx, yy = channel_grid(np)
        wobble = np.sin(7 * xx + frac * math.tau) * np.exp(-xx / 1.4)
        ax0.contourf(field_x, field_y, wobble, levels=28, cmap="RdBu_r")
        ax0.set_title("Channel solve context: oscillations damp after numerical repair", fontsize=12)
        ax0.set_aspect("equal")
        ax0.set_xticks([])
        ax0.set_yticks([])
        ax0.text(0.08, 0.88, "fvSchemes + relaxation", color="white", fontsize=12, weight="bold")

        ax1.semilogy(iters[:n], broken[:n], color="#bc2f2f", lw=2.4, label="broken discretization")
        ax1.semilogy(iters[:n], repaired[:n], color="#0f8a63", lw=2.8, label="repaired setup")
        ax1.axhline(threshold, color="#152033", ls="--", lw=1.5, label=f"threshold {threshold:g}")
        ax1.set_xlim(1, 80)
        ax1.set_ylim(1e-5, 1)
        ax1.set_xlabel("solver iteration")
        ax1.set_ylabel("initial residual")
        ax1.grid(True, which="both", alpha=0.22)
        ax1.legend(loc="upper right")
        ax1.set_title("Residual history under verifier budget", fontsize=12)
        fig.suptitle(report_title(spec, "solver convergence repair"), fontsize=15, weight="bold")
        save_frame(fig, frames_dir, i)


def render_mesh_quality(spec: TaskRenderSpec, frames_dir: Path, frames: int) -> None:
    np, plt = plotting_modules()
    poor_x = graded_positions(np, 20, 4.6)
    fixed_x = graded_positions(np, 20, 1.5)
    y = np.linspace(0, 1, 11)
    for i, frac in enumerate(frame_fracs(frames)):
        blend = smoothstep(frac)
        active_x = (1 - blend) * poor_x + blend * fixed_x
        quality = 0.28 + 0.62 * blend
