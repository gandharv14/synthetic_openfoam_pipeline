@dataclass(frozen=True)
class TaskRenderSpec:
    task_id: str
    family: str
    title: str
    task_dir: Path
    expected_metrics: dict
    hidden_variants: list[dict]
    seed_case: dict
    symptom: str


@dataclass(frozen=True)
class ProblemRender:
    task_id: str
    family: str
    title: str
    path: Path
    caption: str
    marker: str = "render"
    label: str = "Problem visualization"
    aria_label_suffix: str = "reviewer visualization"


def discover_openfoam_tasks(tasks_root: Path) -> list[TaskRenderSpec]:
    """Load generated OpenFOAM tasks from a template problems directory."""
    specs = []
    for report_path in Path(tasks_root).glob("openfoam-*/generation_report.json"):
        specs.append(load_task_spec(report_path.parent))
    return sorted(specs, key=lambda spec: (family_index(spec.family), spec.task_id))


def family_index(family: str) -> int:
    try:
        return FAMILY_ORDER.index(family)
    except ValueError:
        return len(FAMILY_ORDER)


def load_task_spec(task_dir: Path) -> TaskRenderSpec:
    report_path = task_dir / "generation_report.json"
    if not report_path.exists():
        raise FileNotFoundError(f"missing generation report: {report_path}")

    report = json.loads(report_path.read_text())
    manifest_path = task_dir / "data" / "task_manifest.json"
    manifest = json.loads(manifest_path.read_text()) if manifest_path.exists() else {}
    generation = report.get("generation_report", {})
    family = report.get("family") or manifest.get("family") or "unknown"
    return TaskRenderSpec(
        task_id=report.get("task_id", task_dir.name),
        family=family,
        title=generation.get("family_title", family.replace("_", " ").title()),
        task_dir=task_dir,
        expected_metrics=report.get("expected_metrics", {}),
        hidden_variants=report.get("hidden_variants", []),
        seed_case=report.get("seed_case", {}),
        symptom=generation.get("symptom", ""),
    )


def render_all(
    tasks_root: Path,
    assets_dir: Path,
    *,
    task_ids: Iterable[str] | None = None,
    frames: int = 36,
    fps: int = 12,
    skip_missing: bool = False,
) -> list[ProblemRender]:
    selected = set(task_ids or [])
    renders = []
    for spec in discover_openfoam_tasks(tasks_root):
        if selected and spec.task_id not in selected:
            continue
        try:
            renders.append(render_problem(spec.task_dir, assets_dir, frames=frames, fps=fps))
        except ValueError:
            if not skip_missing:
                raise
    return renders


def render_problem(task_dir: Path, assets_dir: Path, *, frames: int = 36, fps: int = 12) -> ProblemRender:
    spec = load_task_spec(task_dir)
    assets_dir.mkdir(parents=True, exist_ok=True)
    output = assets_dir / f"{spec.task_id}.mp4"
    renderer = renderer_for_family(spec.family)
    with tempfile.TemporaryDirectory(prefix=f"{spec.task_id}-render-") as tmp:
        frames_dir = Path(tmp) / "frames"
        frames_dir.mkdir()
        renderer(spec, frames_dir, frames)
        encode_frames(frames_dir, output, fps)
    return ProblemRender(
        task_id=spec.task_id,
        family=spec.family,
        title=spec.title,
        path=output,
        caption=caption_for_family(spec.family),
    )


def renderer_for_family(family: str) -> Callable[[TaskRenderSpec, Path, int], None]:
    renderers = {
        "boundary_condition_repair": render_boundary_condition,
        "solver_convergence_repair": render_solver_convergence,
        "mesh_quality_repair": render_mesh_quality,
        "postprocessing_instrumentation": render_postprocessing,
        "source_term_calibration": render_source_term,
    }
    try:
        return renderers[family]
    except KeyError as exc:
        raise ValueError(f"no renderer for OpenFOAM family: {family}") from exc


def caption_for_family(family: str) -> str:
    return FAMILY_CAPTIONS.get(family, "Synthetic OpenFOAM reviewer visualization for this generated problem.")


def encode_frames(frames_dir: Path, output: Path, fps: int) -> None:
    ffmpeg = shutil.which("ffmpeg")
    if ffmpeg is None:
        raise RuntimeError("ffmpeg is required to render OpenFOAM review videos")
    cmd = [
        ffmpeg,
        "-y",
        "-framerate",
        str(fps),
        "-i",
        str(frames_dir / "frame_%04d.png"),
        "-vf",
        "scale=1280:720:flags=bicubic,format=yuv420p",
        "-c:v",
        "libx264",
        "-preset",
        "medium",
        "-crf",
        "20",
        str(output),
    ]
    result = subprocess.run(cmd, text=True, capture_output=True, check=False)
    if result.returncode != 0:
        raise RuntimeError(f"ffmpeg failed while writing {output}: {result.stderr[-800:]}")


def update_report(
    report_path: Path,
    renders: Iterable[ProblemRender],
    assets_dir: Path,
    *,
    tasks_root: Path | None = None,
    ensure_cards: bool = False,
    current_only: bool = False,
) -> None:
    """Insert or refresh video blocks in the static HTML report."""
    report_path = Path(report_path)
    assets_dir = Path(assets_dir)
    text = report_path.read_text()
    text = ensure_render_css(text)
    specs_by_id = {}
    if tasks_root is not None:
        specs_by_id = {spec.task_id: spec for spec in discover_openfoam_tasks(tasks_root)}
    if current_only and specs_by_id:
        text = current_only_report_shell(text, list(specs_by_id.values()))
    for render in renders:
        spec = specs_by_id.get(render.task_id)
        if ensure_cards and spec is not None:
            text = ensure_report_card(text, spec)
        relative_video = relative_asset_path(report_path, render.path)
        block = render_block(render, relative_video)
        text = replace_render_block(text, render.task_id, block, render.marker)
    report_path.write_text(text)


def ensure_render_css(text: str) -> str:
    if ".render-panel video" in text:
        return text
    anchor = "    .source-list {\n"
    if anchor not in text:
        raise ValueError("could not locate CSS insertion point for render styles")
    return text.replace(anchor, RENDER_CSS + "\n" + anchor, 1)


def relative_asset_path(report_path: Path, asset_path: Path) -> str:
    return Path(asset_path).resolve().relative_to(report_path.resolve().parent).as_posix()


def render_block(render: ProblemRender, relative_video: str) -> str:
    title = html.escape(render.title)
    caption = html.escape(render.caption)
    src = html.escape(relative_video, quote=True)
    label = html.escape(render.label)
    aria = html.escape(f"{render.title} {render.aria_label_suffix}", quote=True)
    marker = html.escape(render.marker, quote=True)
    return f"""          <!-- {marker}:{render.task_id}:start -->
          <section class="section full render-section">
            <div class="label">{label}</div>
            <div class="render-panel">
              <video controls preload="metadata" aria-label="{aria}">
                <source src="{src}" type="video/mp4">
                Your browser does not support embedded video.
              </video>
              <p class="render-caption">{caption}</p>
            </div>
          </section>
          <!-- {marker}:{render.task_id}:end -->
"""


def replace_render_block(text: str, task_id: str, block: str, marker: str = "render") -> str:
    text = remove_existing_render_block(text, task_id, marker)
    task_marker = f"<span class=\"badge blue\"><code>{task_id}</code></span>"
    marker_index = text.find(task_marker)
    if marker_index == -1:
        raise ValueError(f"could not locate report card for {task_id}")
    article_start = text.rfind("<article class=\"card\">", 0, marker_index)
    article_end = text.find("</article>", marker_index)
    if article_start == -1 or article_end == -1:
        raise ValueError(f"could not locate article bounds for {task_id}")
    article = text[article_start:article_end]
    full_section = article.find("          <section class=\"section full\">")
    if full_section == -1:
        raise ValueError(f"could not locate insertion point for {task_id}")
    insert_at = article_start + full_section
    return text[:insert_at] + block + text[insert_at:]


def remove_existing_render_block(text: str, task_id: str, marker: str = "render") -> str:
    start_marker = f"<!-- {marker}:{task_id}:start -->"
    end_marker = f"<!-- {marker}:{task_id}:end -->"
    while True:
        start = text.find(start_marker)
        if start == -1:
            return text
        line_start = text.rfind("\n", 0, start) + 1
        end = text.find(end_marker, start)
        if end == -1:
            return text[:line_start] + text[start:]
        line_end = text.find("\n", end + len(end_marker))
        if line_end == -1:
            line_end = end + len(end_marker)
        else:
            line_end += 1
        text = text[:line_start] + text[line_end:]


def ensure_report_card(text: str, spec: TaskRenderSpec) -> str:
    task_marker = f"<span class=\"badge blue\"><code>{spec.task_id}</code></span>"
    text = remove_report_card(text, task_marker)
    insert_at = cards_insert_position(text)
    return text[:insert_at] + report_card(spec) + text[insert_at:]


def prune_report_cards(text: str, task_ids: set[str]) -> str:
    cards_start = text.rfind("<section class=\"cards\">")
    if cards_start == -1:
        return text
    cards_end = cards_section_end(text, cards_start)
    if cards_end == -1:
        return text
    section = text[cards_start:cards_end]
    kept = []
    for article in re.findall(r"\s*<article class=\"card\">.*?</article>\n?", section, flags=re.S):
        task_id = article_task_id(article)
        if task_id in task_ids:
            kept.append(article)
    next_section = "    <section class=\"cards\">\n" + "".join(kept)
    return text[:cards_start] + next_section + text[cards_end:]


