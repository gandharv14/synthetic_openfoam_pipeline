def render_source_term(spec: TaskRenderSpec, frames_dir: Path, frames: int) -> None:
    np, plt = plotting_modules()
    x, y, xx, yy = channel_grid(np)
    target = float(spec.expected_metrics.get("source_target", 0.2) or 0.2)
    tolerance = float(spec.expected_metrics.get("tolerance", 0.05) or 0.05)
    t = np.linspace(0, 1, frames)
    response = target - 0.12 * np.exp(-4.0 * t)
    for i, frac in enumerate(frame_fracs(frames)):
        source_gain = 0.35 + 0.65 * smoothstep(frac)
        scalar = source_gain * np.exp(-((xx - 0.75) ** 2 / 0.09 + (yy - 0.5) ** 2 / 0.16))
        scalar += 0.18 * xx + 0.04 * np.sin(2 * np.pi * (xx - frac))
        n = max(2, i + 1)
        fig, (ax0, ax1) = plt.subplots(1, 2, figsize=(12.8, 7.2), dpi=100, constrained_layout=True)
        im = ax0.contourf(x, y, scalar, levels=30, cmap="inferno")
        ax0.add_patch(plt.Rectangle((0.55, 0.2), 0.4, 0.6, fill=False, edgecolor="white", lw=2.5))
        ax0.text(0.75, 0.84, "fvOptions source region", color="white", ha="center", fontsize=11, weight="bold")
        ax0.set_title("Scalar channel response field", fontsize=12)
        ax0.set_aspect("equal")
        ax0.set_xticks([])
        ax0.set_yticks([])
        fig.colorbar(im, ax=ax0, fraction=0.046, pad=0.025, label="scalar response")
        ax1.axhspan(target - tolerance, target + tolerance, color="#e8f7f1", label="target tolerance band")
        ax1.axhline(target, color="#0f8a63", lw=2, label=f"target {target:g}")
        ax1.plot(t[:n], response[:n], color="#2f6fed", lw=3, label="fieldAverage response")
        ax1.scatter([t[n - 1]], [response[n - 1]], color="#2f6fed", s=55)
        ax1.set_xlim(0, 1)
        ax1.set_ylim(target - 0.16, target + 0.09)
        ax1.set_xlabel("calibration progress")
        ax1.set_ylabel("field average")
        ax1.grid(True, alpha=0.22)
        ax1.legend(loc="lower right")
        ax1.set_title("Source calibration metric", fontsize=12)
        fig.suptitle(report_title(spec, "source-term calibration"), fontsize=15, weight="bold")
        save_frame(fig, frames_dir, i)


def plotting_modules():
    import matplotlib

    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    import numpy as np

    return np, plt


def channel_grid(np):
    x = np.linspace(0, 2, 150)
    y = np.linspace(0, 1, 80)
    xx, yy = np.meshgrid(x, y)
    return x, y, xx, yy


def frame_phases(frames: int):
    for idx in range(frames):
        yield math.tau * idx / max(frames, 1)


def frame_fracs(frames: int):
    for idx in range(frames):
        yield idx / max(frames - 1, 1)


def save_frame(fig, frames_dir: Path, index: int) -> None:
    fig.savefig(frames_dir / f"frame_{index:04d}.png", dpi=100)
    fig.clear()
    import matplotlib.pyplot as plt

    plt.close(fig)


def report_title(spec: TaskRenderSpec, focus: str) -> str:
    seed = spec.seed_case.get("seed_id", "synthetic case")
    return f"{spec.title} - {focus} ({seed})"


def smoothstep(value: float) -> float:
    value = max(0.0, min(1.0, value))
    return value * value * (3 - 2 * value)


def graded_positions(np, count: int, ratio: float):
    weights = ratio ** np.linspace(0, 1, count)
    widths = weights / weights.sum()
    return np.concatenate(([0.0], np.cumsum(widths)))


def draw_grid(ax, x_positions, y_positions, color: str, label: str, *, alpha: float = 1.0) -> None:
    for x in x_positions:
        ax.plot([x, x], [0, 1], color=color, alpha=alpha, lw=1.1)
    for y in y_positions:
        ax.plot([0, 1], [y, y], color=color, alpha=alpha, lw=1.1)
    ax.set_xlim(-0.02, 1.02)
    ax.set_ylim(-0.02, 1.02)
    ax.set_aspect("equal")
    ax.set_xticks([])
    ax.set_yticks([])
    ax.text(0.03, 0.96, label, color=color, fontsize=11, weight="bold", va="top")
